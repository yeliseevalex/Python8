# Визначаємо логін та пароль для перевірки
login = "admin"
password = "password1234"

# Вводимо логін та пароль від користувача
user_login = input("Enter login: ")
user_password = input("Enter password: ")

# Перевірка введеного логіну та паролю
if user_login == login and user_password == password:
    print("Успішний вхід")  # Якщо логін і пароль правильні
else:
    print("Невірний логін або пароль")  # Якщо введені дані не співпадають

# Ініціалізуємо порожній список для зберігання результатів обчислень
arr = []

try:
    # Виконуємо кілька математичних операцій та додаємо їх результати в список
    a = 10 / 5
    b = 10 / 2
    arr.append(a)  # Додаємо результат 10/5 до списку
    arr.append(b)  # Додаємо результат 10/2 до списку

    # Тут ми намагаємося додати рядок до числа, що викликає помилку
    # Щоб уникнути помилки типу, перетворюємо "5" в ціле число
    c = int("5") + 5  # Перетворюємо рядок "5" в ціле число та додаємо до 5
    arr.append(c)  # Додаємо результат до списку

    # Заміщаємо ділення на нуль (яке викликало б помилку) на безпечне ділення
    print(10 / 10)  # Виводимо результат ділення 10 на 10
    print(10 / 1)  # Виводимо результат ділення 10 на 1

# Обробка виключень для конкретних помилок
except ZeroDivisionError as ex:
    # Якщо сталася помилка ділення на нуль, виводимо відповідне повідомлення
    print(f"Помилка: Ділення на нуль - {ex}")
except ValueError as ex:
    # Якщо сталася помилка при перетворенні типу даних (наприклад, рядок до числа)
    print(f"Помилка: Невірний тип даних - {ex}")
except Exception as ex:
    # Якщо сталася інша невідома помилка, виводимо її
    print(f"Невідома помилка: {ex}")

# Блок finally виконується завжди, навіть якщо сталася помилка
finally:
    # Виводимо результат обчислень, що були збережені в списку
    print(f"Завершення програми. Результати обчислень: {arr}")
